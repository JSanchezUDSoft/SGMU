package Controller;


public class Cliente {
    private String  tipdocc;
    private String  numdocc;
    private String  n1;
    private String  n2;
    private String  a1;
    private String  a2;
    private String  correoc;
    private long    celular;
    private String  dresidencia;

    public Cliente() {
    }

    public String getTipdocc() {
        return tipdocc;
    }

    public void setTipdocc(String tipdocc) {
        this.tipdocc = tipdocc;
    }

    public String getNumdocc() {
        return numdocc;
    }

    public void setNumdocc(String numdocc) {
        this.numdocc = numdocc;
    }

    public String getN1() {
        return n1;
    }

    public void setN1(String n1) {
        this.n1 = n1;
    }

    public String getN2() {
        return n2;
    }

    public void setN2(String n2) {
        this.n2 = n2;
    }

    public String getA1() {
        return a1;
    }

    public void setA1(String a1) {
        this.a1 = a1;
    }

    public String getA2() {
        return a2;
    }

    public void setA2(String a2) {
        this.a2 = a2;
    }

    public String getCorreoc() {
        return correoc;
    }

    public void setCorreoc(String correoc) {
        this.correoc = correoc;
    }

    public long getCelular() {
        return celular;
    }

    public void setCelular(long celular) {
        this.celular = celular;
    }

    public String getDresidencia() {
        return dresidencia;
    }

    public void setDresidencia(String dresidencia) {
        this.dresidencia = dresidencia;
    }      
}
